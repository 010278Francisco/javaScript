/**Operadores relacionales o de comparacion */

/**Mayor que : a > b  -----a es mayor que b 
 * Menor que : a < b ------a en menor que b 
 * Mayor o igual que a >== b ---- a es mayor o igual que b
 * Mayor o igual que a <== b ---- a es menor o igual que b
 * igual a == b ----- a es igual a b
 * distinto a != b ------a es diferente a, b 
 * operador estricto ===
 * operador diferente estricto 
*/

let numeroUno = 22;
let numeroDos = 15;


console.log(numeroUno > numeroDos);
console.log(numeroUno < numeroDos);
console.log(numeroUno >= numeroDos);
console.log(numeroUno <= numeroDos);
console.log(numeroUno == numeroDos);
console.log(numeroUno != numeroDos);